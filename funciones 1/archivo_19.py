def es_numero_positivo_ELKIN_ALEXIS_MORENO_ROJAS(numero):
    if numero > 0:
        print(f"El número {numero} es positivo.")
    elif numero < 0:
        print(f"El número {numero} es negativo.")
    else:
        print("El número es cero.")

