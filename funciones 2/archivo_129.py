def es_subconjunto_ELKIN_ALEXIS_MORENO_ROJAS():
    conjunto1 = {1, 2}
    conjunto2 = {1, 2, 3, 4}
    if conjunto1.issubset(conjunto2):
        print("El primer conjunto es subconjunto del segundo.")
    else:
        print("El primer conjunto no es subconjunto del segundo.")

es_subconjunto_ELKIN_ALEXIS_MORENO_ROJAS()

